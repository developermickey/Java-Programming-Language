public class JavaDataTypes {
    public static void main(String args[]) {
        /*
         * Data types in Java
         * Primitive types
         * byte - 1 bytes  [-128 to 127]
         * short - 2 bytes  [-128 to 127]
         * char - 2 bytes  [-128 to 127]
         * boolean - 1 bytes  [-128 to 127]
         * int - 4 bytes  [-128 to 127]
         * long - 8 bytes  [-128 to 127]
         * float - 4 bytes  [-128 to 127]
         * double - 8 bytes  [-128 to 127]
         * Existing Hote hai  
         */

         byte b = 8;
         System.out.println(b);

         char ch = 'a';
         System.out.println(ch);

         boolean mickey = false;
         System.out.println(mickey);

         float sas = 5.75f;
         System.out.println(sas);





         /*
         * Non-Primitive types
         * String 
         * Array 
         * Object
         * Class
         * Interface
         * Create karna padta hia inhe
         * 
         * Java Me her ek cheez ko Define karna padata hai 
         * Like String, Int, Float, Double, Long, Array hume types define karne padete hai otherwise error show hogi 
         */
    }
}
